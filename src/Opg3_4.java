public class Opg3_4 {
    public static void main(String[] args) {

        int month = (int)(Math.random() * 12);


        switch ( month ) {
            case 0: System.out.println("januar"); break;
            case 1: System.out.println("februar"); break;
            case 2: System.out.println("marts"); break;
            case 3: System.out.println("april"); break;
            case 4: System.out.println("maj"); break;
            case 5: System.out.println("juni"); break;
            case 6: System.out.println("juli"); break;
            case 7: System.out.println("august"); break;
            case 8: System.out.println("september"); break;
            case 9: System.out.println("oktober"); break;
            case 10: System.out.println("november"); break;
            case 11: System.out.println("december"); break;
        }


    }
}
